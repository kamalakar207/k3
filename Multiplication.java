package com.prutech.authentication;

/**
 * Multiplication function class
 * 
 * @author Admin
 *
 */
public class Multiplication {
	/**
	 * multiply two numbers
	 * 
	 * @param a
	 * @param b
	 */
	public static Double multiply(Double a, Double b) {
		Double c = a * b;
		return c;
	}
}
